public class IfElse {
    public static void main(String[] args) {
        String city = "Kathmandu";
        if (city == "Pokhara") {
            System.out.println("Your city is Pokhara.");
        } else if (city == "Kathmandu") {
            System.out.println("Your city is Kathmandu.");
        } else if (city == "Dhading") {
            System.out.println("Your city is Dhading.");
        } else {
            System.out.println(city);
        }
    }
}
